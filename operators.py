# Python Arithmetic Operators
print("Python Arithmetic Operators")

# + addition x + y 
# - subtraction x - y 
# * multiplication x * y 
# / division  x / y 
# % Modulus x % y
# ** exponentiation x ** y x = 2, y= 5 same as 2*2*2*2*2
# // Floor division x // y  the floor division // rounds the result down to the nearest whole number

print(100 + 20)
print(100 - 20)
print(100 * 20)
print(100 / 20)
print(100 % 20)
print(100 ** 20)
